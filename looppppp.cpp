#include<iostream>

using namespace std;

int main(){
	
for(int x = 0;  x < 10 ; x++) {
  cout << x<<"\n";
}

for(int x = 10;  x > 0 ; x--) {
  cout << x<<"\n";
}

for(char x = 'a';  x <= 'j' ; x++) {
  cout << x<<"\n";
}

for(char o = 'j';  o >= 'a' ; o--) {
  cout << o<<"\n";
}

for(int x = 1;  x <= 100 ; x= x+2) {
  cout << x<<"\n";
}

for(int x = 0;  x <= 100 ; x= x+2) {
  cout << x<<"\n";
}
}
